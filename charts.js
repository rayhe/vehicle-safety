// ================================================================
// FARS Per-Model: filters & sort
// ================================================================
function applyFarsFilters() {
  const search = document.getElementById('fmSearch').value.toLowerCase();
  const clsFilter = document.getElementById('fmClassFilter').value;
  const sortMode = document.getElementById('fmSort').value;

  filteredFars = FARS_BY_MODEL.filter(d => {
    if (farsMetric === 'rate' && (d.rate === null || d.rate === undefined)) return false;
    if (search && !farsVehicleName(d).toLowerCase().includes(search) && !d.cls.toLowerCase().includes(search)) return false;
    if (clsFilter && d.cls !== clsFilter) return false;
    return true;
  });

  switch (sortMode) {
    case 'deaths-desc':
      filteredFars.sort((a, b) => b.deaths - a.deaths);
      break;
    case 'rate-desc':
      filteredFars.sort((a, b) => (b.rate || 0) - (a.rate || 0));
      break;
    case 'rate-asc':
      filteredFars.sort((a, b) => (a.rate || 999) - (b.rate || 999));
      break;
    case 'make':
      filteredFars.sort((a, b) => farsVehicleName(a).localeCompare(farsVehicleName(b)));
      break;
  }

  renderFarsModelChart();
  renderFarsModelTable();
  updateFarsSummary();
}

