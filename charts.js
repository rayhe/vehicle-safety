function renderNationalChart() {
  const canvas = document.getElementById('nationalChart');
  const ctx = canvas.getContext('2d');
  const dpr = window.devicePixelRatio || 1;
  const isDark = document.documentElement.classList.contains('dark');
  const textColor = isDark ? '#e0e0e0' : '#1a1a1a';
  const mutedColor = isDark ? '#999' : '#666';
  const gridColor = isDark ? 'rgba(255,255,255,0.08)' : 'rgba(0,0,0,0.06)';
  const font = getComputedStyle(document.body).fontFamily;

  const wrap = canvas.parentElement;
  const W = wrap.clientWidth - 32;
  const H = Math.min(300, Math.max(220, W * 0.45));

  canvas.style.width = W + 'px';
  canvas.style.height = H + 'px';
  canvas.width = W * dpr;
  canvas.height = H * dpr;
  ctx.scale(dpr, dpr);
  ctx.clearRect(0, 0, W, H);

  const padL = 60, padR = 55, padT = 20, padB = 40;
  const plotW = W - padL - padR;
  const plotH = H - padT - padB;
  const n = FARS_NATIONAL.length;
  const barW = Math.min(50, plotW / n * 0.55);
  const gap = plotW / n;

  // Scales
  const maxFat = Math.max(...FARS_NATIONAL.map(d => d.fatalities)) * 1.12;
  const minRate = Math.min(...FARS_NATIONAL.map(d => d.rate)) - 0.05;
  const maxRate = Math.max(...FARS_NATIONAL.map(d => d.rate)) + 0.05;

  function yFat(v) { return padT + plotH - (v / maxFat) * plotH; }
  function yRate(v) { return padT + plotH - ((v - minRate) / (maxRate - minRate)) * plotH; }
  function xCenter(i) { return padL + gap * i + gap / 2; }

  // Grid lines (fatalities axis)
  ctx.strokeStyle = gridColor;
  ctx.lineWidth = 1;
  for (let i = 0; i <= 4; i++) {
    const v = (maxFat / 4) * i;
    const y = yFat(v);
    ctx.beginPath();
    ctx.moveTo(padL, y);
    ctx.lineTo(W - padR, y);
    ctx.stroke();
  }

  // Bars
  FARS_NATIONAL.forEach((d, i) => {
    const x = xCenter(i) - barW / 2;
    const y = yFat(d.fatalities);
    const h = yFat(0) - y;
    ctx.fillStyle = d.estimate
      ? getComputedStyle(document.documentElement).getPropertyValue('--fars-estimate').trim()
      : getComputedStyle(document.documentElement).getPropertyValue('--fars-bar').trim();
    ctx.beginPath();
    ctx.roundRect(x, y, barW, h, [3, 3, 0, 0]);
    ctx.fill();

    // Year label
    ctx.fillStyle = mutedColor;
    ctx.font = '10px ' + font;
    ctx.textAlign = 'center';
    ctx.fillText(d.year + (d.estimate ? '*' : ''), xCenter(i), H - padB + 16);

    // Fatality count on bar
    ctx.fillStyle = textColor;
    ctx.font = '10px ' + font;
    ctx.fillText((d.fatalities / 1000).toFixed(1) + 'k', xCenter(i), y - 6);
  });

  // Line (rate)
  const lineColor = getComputedStyle(document.documentElement).getPropertyValue('--fars-line').trim();
  ctx.strokeStyle = lineColor;
  ctx.lineWidth = 2.5;
  ctx.lineJoin = 'round';
  ctx.beginPath();
  FARS_NATIONAL.forEach((d, i) => {
    const x = xCenter(i);
    const y = yRate(d.rate);
    if (i === 0) ctx.moveTo(x, y); else ctx.lineTo(x, y);
  });
  ctx.stroke();

  // Dots + rate labels
  FARS_NATIONAL.forEach((d, i) => {
    const x = xCenter(i);
    const y = yRate(d.rate);
    ctx.fillStyle = lineColor;
    ctx.beginPath();
    ctx.arc(x, y, 4, 0, Math.PI * 2);
    ctx.fill();
    ctx.fillStyle = isDark ? '#fff' : lineColor;
    ctx.beginPath();
    ctx.arc(x, y, 2, 0, Math.PI * 2);
    ctx.fill();

    ctx.fillStyle = lineColor;
    ctx.font = 'bold 10px ' + font;
    ctx.textAlign = 'center';
    ctx.fillText(d.rate.toFixed(2), x, y - 10);
  });

  // Left axis label
  ctx.save();
  ctx.fillStyle = mutedColor;
  ctx.font = '9px ' + font;
  ctx.translate(12, padT + plotH / 2);
  ctx.rotate(-Math.PI / 2);
  ctx.textAlign = 'center';
  ctx.fillText('Total Fatalities', 0, 0);
  ctx.restore();

  // Right axis label
  ctx.save();
  ctx.fillStyle = lineColor;
  ctx.font = '9px ' + font;
  ctx.translate(W - 8, padT + plotH / 2);
  ctx.rotate(Math.PI / 2);
  ctx.textAlign = 'center';
  ctx.fillText('Rate / 100M VMT', 0, 0);
  ctx.restore();

  // Left axis ticks
  ctx.fillStyle = mutedColor;
  ctx.font = '9px ' + font;
  ctx.textAlign = 'right';
  for (let i = 0; i <= 4; i++) {
    const v = (maxFat / 4) * i;
    ctx.fillText(Math.round(v / 1000) + 'k', padL - 6, yFat(v) + 3);
  }

  // Right axis ticks
  ctx.fillStyle = lineColor;
  ctx.textAlign = 'left';
  const rSteps = 4;
  for (let i = 0; i <= rSteps; i++) {
    const v = minRate + ((maxRate - minRate) / rSteps) * i;
    ctx.fillText(v.toFixed(2), W - padR + 6, yRate(v) + 3);
  }

  // Hit zones
  hitZones.national = FARS_NATIONAL.map((d, i) => ({
    x: xCenter(i) - gap/2, y: padT, w: gap, h: plotH,
    html: '<b>' + d.year + (d.estimate ? ' (est.)' : '') + '</b><br>' +
      'Fatalities: ' + d.fatalities.toLocaleString() + '<br>' +
      'VMT: ' + d.vmt.toFixed(2) + ' trillion<br>' +
      'Rate: ' + d.rate.toFixed(2) + ' per 100M VMT'
  }));
}

