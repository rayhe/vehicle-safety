function farsClsColor(cls) {
  const cs = getComputedStyle(document.documentElement);
  switch (cls) {
    case 'Sedan':      return cs.getPropertyValue('--cls-sedan').trim();
    case 'SUV':        return cs.getPropertyValue('--cls-suv').trim();
    case 'Pickup':     return cs.getPropertyValue('--cls-pickup').trim();
    case 'Van':        return cs.getPropertyValue('--cls-van').trim();
    case 'Sports Car': return cs.getPropertyValue('--cls-sports').trim();
    default:           return '#999';
  }
}

