function applyToxFilters() {
  if (!FARS_TOXICOLOGY || FARS_TOXICOLOGY.length === 0) return;
  const search = document.getElementById('toxSearch').value.toLowerCase();
  const clsFilter = document.getElementById('toxClassFilter').value;
  const sortMode = document.getElementById('toxSort').value;

  filteredTox = FARS_TOXICOLOGY.filter(d => {
    if (search && !(d.make + ' ' + d.model).toLowerCase().includes(search) && !d.cls.toLowerCase().includes(search)) return false;
    if (clsFilter && d.cls !== clsFilter) return false;
    return true;
  });

  switch (sortMode) {
    case 'any-desc': filteredTox.sort((a, b) => b.anyPct - a.anyPct); break;
    case 'alc-desc': filteredTox.sort((a, b) => b.alcPct - a.alcPct); break;
    case 'drug-desc': filteredTox.sort((a, b) => b.drugPct - a.drugPct); break;
    case 'any-asc': filteredTox.sort((a, b) => a.anyPct - b.anyPct); break;
    case 'any-raw-desc': filteredTox.sort((a, b) => b.any - a.any); break;
    case 'alc-raw-desc': filteredTox.sort((a, b) => b.alc - a.alc); break;
    case 'drug-raw-desc': filteredTox.sort((a, b) => b.drug - a.drug); break;
    case 'drivers-desc': filteredTox.sort((a, b) => b.drivers - a.drivers); break;
    case 'make': filteredTox.sort((a, b) => (a.make + ' ' + a.model).localeCompare(b.make + ' ' + b.model)); break;
  }

  renderToxChart();
  renderToxTable();
  updateToxSummary();
}

function updateToxSummary() {
  if (!FARS_TOXICOLOGY || FARS_TOXICOLOGY.length === 0) return;
  const totalDrivers = FARS_TOXICOLOGY.reduce((s, d) => s + d.drivers, 0);
  const totalAny = FARS_TOXICOLOGY.reduce((s, d) => s + d.any, 0);
  document.getElementById('toxOverallRate').textContent = (totalAny / totalDrivers * 100).toFixed(1) + '%';

  const sorted = [...FARS_TOXICOLOGY].sort((a, b) => b.anyPct - a.anyPct);
  document.getElementById('toxHighest').textContent = sorted[0].make + ' ' + sorted[0].model;
  document.getElementById('toxHighestDetail').textContent = sorted[0].anyPct + '% any impairment';
  const last = sorted[sorted.length - 1];
  document.getElementById('toxLowest').textContent = last.make + ' ' + last.model;
  document.getElementById('toxLowestDetail').textContent = last.anyPct + '% any impairment';
}

