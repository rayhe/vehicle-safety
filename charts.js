function renderMyearChart() {
  const canvas = document.getElementById('myearChart');
  const ctx = canvas.getContext('2d');
  const dpr = window.devicePixelRatio || 1;
  const isDark = document.documentElement.classList.contains('dark');
  const textColor = isDark ? '#e0e0e0' : '#1a1a1a';
  const mutedColor = isDark ? '#999' : '#666';
  const gridColor = isDark ? 'rgba(255,255,255,0.08)' : 'rgba(0,0,0,0.06)';
  const font = getComputedStyle(document.body).fontFamily;
  const cs = getComputedStyle(document.documentElement);

  const wrap = canvas.parentElement;
  const W = wrap.clientWidth - 32;
  const H = Math.min(350, Math.max(250, W * 0.45));

  canvas.style.width = W + 'px';
  canvas.style.height = H + 'px';
  canvas.width = W * dpr;
  canvas.height = H * dpr;
  ctx.scale(dpr, dpr);
  ctx.clearRect(0, 0, W, H);

  if (myearSelected.length === 0 || !FARS_MODEL_YEAR || FARS_MODEL_YEAR.length === 0) {
    ctx.fillStyle = mutedColor;
    ctx.font = '13px ' + font;
    ctx.textAlign = 'center';
    ctx.fillText('Search and select vehicles above to compare model years', W / 2, H / 2);
    return;
  }

  // Gather data series
  const series = myearSelected.map((sel, i) => {
    const entry = FARS_MODEL_YEAR.find(d => d.make === sel.make && d.model === sel.model);
    return {
      label: sel.make + ' ' + sel.model,
      color: cs.getPropertyValue(MYEAR_COLORS[i]).trim(),
      years: entry ? entry.years : {},
    };
  });

  // Find year range and max value
  let allYears = new Set();
  let maxVal = 0;
  series.forEach(s => {
    Object.entries(s.years).forEach(([y, d]) => {
      allYears.add(parseInt(y));
      if (d > maxVal) maxVal = d;
    });
  });
  const years = [...allYears].sort((a, b) => a - b);
  if (years.length === 0) return;
  maxVal *= 1.1;

  const padL = 55, padR = 20, padT = 20, padB = 40;
  const plotW = W - padL - padR;
  const plotH = H - padT - padB;

  function xPos(yr) { return padL + ((yr - years[0]) / (years[years.length - 1] - years[0] || 1)) * plotW; }
  function yPos(v) { return padT + plotH - (v / maxVal) * plotH; }

  // Grid
  ctx.strokeStyle = gridColor;
  ctx.lineWidth = 1;
  for (let i = 0; i <= 4; i++) {
    const v = (maxVal / 4) * i;
    const y = yPos(v);
    ctx.beginPath();
    ctx.moveTo(padL, y);
    ctx.lineTo(W - padR, y);
    ctx.stroke();
  }

  // Y axis ticks
  ctx.fillStyle = mutedColor;
  ctx.font = '9px ' + font;
  ctx.textAlign = 'right';
  for (let i = 0; i <= 4; i++) {
    const v = (maxVal / 4) * i;
    ctx.fillText(Math.round(v), padL - 6, yPos(v) + 3);
  }

  // X axis labels
  ctx.textAlign = 'center';
  const yearStep = Math.max(1, Math.floor(years.length / 10));
  years.forEach((yr, i) => {
    if (i % yearStep === 0 || yr === years[years.length - 1]) {
      ctx.fillText(yr, xPos(yr), H - padB + 16);
    }
  });

  // Draw lines
  series.forEach(s => {
    const pts = years.filter(y => s.years[y] !== undefined).map(y => ({ x: xPos(y), y: yPos(s.years[y]) }));
    if (pts.length < 2) return;

    ctx.strokeStyle = s.color;
    ctx.lineWidth = 2.5;
    ctx.lineJoin = 'round';
    ctx.beginPath();
    pts.forEach((p, i) => { if (i === 0) ctx.moveTo(p.x, p.y); else ctx.lineTo(p.x, p.y); });
    ctx.stroke();

    // Dots
    pts.forEach(p => {
      ctx.fillStyle = s.color;
      ctx.beginPath();
      ctx.arc(p.x, p.y, 3, 0, Math.PI * 2);
      ctx.fill();
    });
  });

  // Y axis label
  ctx.save();
  ctx.fillStyle = mutedColor;
  ctx.font = '9px ' + font;
  ctx.translate(12, padT + plotH / 2);
  ctx.rotate(-Math.PI / 2);
  ctx.textAlign = 'center';
  ctx.fillText('Deaths', 0, 0);
  ctx.restore();

  // Hit zones — one per data point
  hitZones.myear = [];
  series.forEach(s => {
    years.forEach(yr => {
      if (s.years[yr] === undefined) return;
      const deaths = s.years[yr];
      hitZones.myear.push({
        x: xPos(yr) - 8, y: yPos(deaths) - 8, w: 16, h: 16,
        html: '<b>' + s.label + '</b><br>' +
          'Model year: ' + yr + '<br>' +
          'Deaths: ' + deaths.toLocaleString()
      });
    });
  });
}

function updateMyearSummary() {
  if (!FARS_MODEL_YEAR || FARS_MODEL_YEAR.length === 0) return;
  let minYear = 9999, maxYear = 0;
  const yearTotals = {};
  FARS_MODEL_YEAR.forEach(d => {
    Object.entries(d.years).forEach(([y, deaths]) => {
      const yr = parseInt(y);
      if (yr < minYear) minYear = yr;
      if (yr > maxYear) maxYear = yr;
      yearTotals[yr] = (yearTotals[yr] || 0) + deaths;
    });
  });
  document.getElementById('myearRange').textContent = minYear + '\u2013' + maxYear;
  const peakEntry = Object.entries(yearTotals).sort((a, b) => b[1] - a[1])[0];
  if (peakEntry) {
    document.getElementById('myearPeak').textContent = peakEntry[0];
    document.getElementById('myearPeakDetail').textContent = peakEntry[1].toLocaleString() + ' deaths across all models';
  }
}

